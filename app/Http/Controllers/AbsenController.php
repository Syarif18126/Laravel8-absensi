    {
        //
        $this->validate($request, [
            'nama' => 'required',
            'nis' => 'required',
            'kelas' => 'required',
            'keahlian' => 'required',
            'keterangan' => 'required',
        ]);

        $input = $request->all();
        Absen::create($input);
